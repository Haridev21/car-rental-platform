// Get car by ID
export const getCarById = (id) => {
    return cars.find(car => car.id === parseInt(id))
}

// Get cars by type
export const getCarsByType = (type) => {
    return cars.filter(car => car.type === type)
}

// Get featured cars (popular or recommended)
export const getFeaturedCars = (limit = 8) => {
    return cars
        .filter(car => car.popular || car.recommended)
        .slice(0, limit)
}

// Get similar cars (same type, different id)
export const getSimilarCars = (carId, limit = 4) => {
    const car = getCarById(carId)
    if (!car) return []
    return cars
        .filter(c => c.type === car.type && c.id !== car.id)
        .slice(0, limit)
}

